#ifndef TRIANGLE_H_
#define TRIANGLE_H_

class Point
{
    public:
        Point();
        ~Point();
        Point(float x,float y);
        void Show() const;//const成员函数,保证函数不会修改调用对象
        float Set(const Point & s);
    private:
        float _x;
        float _y;
        
};


class Triangle
{
    public:
        Triangle(float a=0,float b=0,float c=0);
        Triangle(const Point & p1,const Point & p2,const Point & p3);
        ~Triangle();
        void Show();
        void SetX();
        void SetY();
        float Area();
    private:
        float _a;
        float _b;
        float _c;
        float _S;
        float _p;
        float _x;
        float _y;
        void C(){_p=(_a+_b+_c)/2;}
        Point p1;
        Point p2;
        Point p3;
        bool index=0;
}; 

#endif